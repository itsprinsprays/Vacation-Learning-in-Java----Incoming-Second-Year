package Day1_Methods;

public class print {
	
	public static void p(String statement) {
		System.out.print(statement);
	}
	
	public static void p(int statement) {
		System.out.print(statement);
	}
	
	public static void pl(String statement) {
		System.out.println(statement);
	}
	
	public static void pl(int statement) {
		System.out.println(statement);
	}


}
